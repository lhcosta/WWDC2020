 # **THE JOINER DAY**
 
 O que faz um marceneiro ? 
 Este profissional é responsável pela construção e reparo de móveis, instalação de portas e janelas, possui técnicas em revestimentos e acabamentos em madeira, tem habilidades de manuseio de diversas ferramentas de corte e montagem.

 A marcenaria é uma profissão que exige bastante habilidade e muita concentração do profissional, e existem algumas etapas básicas que são importantes para o início nesta carreira. Vamos aprender algumas delas ?

 * * * *
 ### As 3 principais atividades de um marceneiro:
 * * * *
 
 * Corte - é importante este profissional realizar os cortes dos tamanhos corretos e dos formatos necessários para a criação de um móvel. Nesta etapa é preciso ter uma maior atenção, pois os acidentes são bastantes frequentes.

 * Colagem - para a montagem de diversos móveis, a colagem é uma etapa que assegura a resistência e também é utilizado para o acabamento final.

 * Fixação - para assegurar a durabilidade dos nossos projetos é de extrema importância a fixação através de pregos/parafusos, mesmo passando pela etapa de colagem é importante dar uma maior segurança na montagem.
 
 * * * *
 ## Instruções  
    1. Corte, nesta fase você movimentará o iPad para frente para subida e para trás para decida da serra.
 
    2. Fixação, seu objeto é martelar os pregos, utilizando o botão HIT.

    3. Colagem, seu objeto é acertar as marcas com a cola, utilizando o botão HIT.
 
 * * * *
 - Note: 
    Para uma melhor experiência, entre em modo fullscreen.
 * * * *
 
